/* 

A callback function is a function that is passed as an argument to another function 
and is executed after some operation has been completed. Callbacks are used to handle 
asynchronous operations, like data fetching, setTimeout, and handling events.


Why Use Callback Functions?
Callbacks are mainly used to:

Ensure that code runs only after a specific task is completed.
Avoid blocking the execution of other code while waiting for a long-running operation to finish.
Handle asynchronous operations.


*/